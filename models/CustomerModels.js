const mongoose = require("mongoose");

const CustomerSchema = mongoose.Schema(
  {
    firstName: {
      type: String,
      required: [true,"Please add a firstname"],
    },
    lastName: {
      type: String,
      required: true,
    },
    userName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    phone: {
      type: String,
      required: true,
    },
    dob: {
      type: String,
      required: true,
    },
    gender: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
    landmark: {
      type: String,
      required: true,
    },
    city: {
      type: String,
      required: true,
    },
    state: {
      type: String,
      required: true,
    },
    country: {
      type: String,
      required: true,
    },
    zipcode: {
      type: Number,
      required: true,
    },
  },
  { timestamps: true }
);

const CustomerModels = mongoose.model("customers", CustomerSchema);

module.exports = CustomerModels;
